async function jsonOrNull(url, signal) {
  try {
    const r = await fetch(url, { signal, headers: { accept: 'application/json' } });
    if (!r.ok) return null;
    return await r.json();
  } catch {
    return null;
  }
}

async function postJsonOrNull(url, body, signal) {
  try {
    const r = await fetch(url, {
      method: 'POST',
      signal,
      headers: { 'content-type': 'application/json', accept: 'application/json' },
      body: JSON.stringify(body),
    });
    if (!r.ok) return null;
    return await r.json();
  } catch {
    return null;
  }
}

async function textOrNull(url, signal) {
  try {
    const r = await fetch(url, { signal });
    if (!r.ok) return null;
    const body = await r.text();
    // The endpoint returns JSON on error (e.g. insufficient history); only accept SVG.
    return body.includes('<svg') ? body : null;
  } catch {
    return null;
  }
}

// Defense-in-depth before inlining an SVG into the page. The SVG is generated by
// our own matplotlib endpoint (which XML-escapes label text and never emits
// scripts), but we still strip any active content as a sink-side guard.
function sanitizeSvg(svg) {
  if (!svg || !svg.includes('<svg')) return null;
  return svg
    .replace(/<script[\s\S]*?<\/script>/gi, '')
    .replace(/<foreignObject[\s\S]*?<\/foreignObject>/gi, '')
    .replace(/\son\w+\s*=\s*"[^"]*"/gi, '')
    .replace(/\son\w+\s*=\s*'[^']*'/gi, '')
    .replace(/(?:xlink:href|href)\s*=\s*(["'])\s*javascript:[^"']*\1/gi, '');
}

async function resolveCompanyName(slug, signal) {
  const data = await jsonOrNull(
    `${API_BASE}/bormes/companies/directory/autocomplete?q=${encodeURIComponent(slugToQuery(slug))}&limit=10`,
    signal,
  );
  const suggestions = (data && data.suggestions) || [];
  if (!suggestions.length) return null;
  const exact = suggestions.find((s) => nameToSlug(s.company_name) === slug);
  return exact || suggestions[0];
}

